document.addEventListener('DOMContentLoaded', () => {
    // Current state
    let activeTab = 'overview';
    let chartsList = [];
    let fullDataset = [];
    let tableColumns = [];
    let pipelinePollInterval = null;
    let isRunning = false;

    // DOM Elements
    const navLinks = document.querySelectorAll('.nav-link');
    const tabPanes = document.querySelectorAll('.tab-pane');
    const tabTitle = document.getElementById('tab-title');
    const tabDesc = document.getElementById('tab-desc');
    const lastUpdated = document.getElementById('last-updated');
    
    // Controls
    const btnRefresh = document.getElementById('btn-refresh');
    const refreshIcon = document.getElementById('refresh-icon');
    const btnRunPipeline = document.getElementById('btn-run-pipeline');
    const pipelineStatusDot = document.getElementById('pipeline-status-dot');
    const pipelineStatusText = document.getElementById('pipeline-status-text');
    
    // KPI slots
    const kpiCustomers = document.getElementById('kpi-customers');
    const kpiLoans = document.getElementById('kpi-loans');
    const kpiAmount = document.getElementById('kpi-amount');
    const kpiDefault = document.getElementById('kpi-default');
    const kpiCredit = document.getElementById('kpi-credit');
    const kpiRate = document.getElementById('kpi-rate');
    
    // Breakdowns
    const riskBreakdown = document.getElementById('risk-breakdown');
    const statusBreakdown = document.getElementById('status-breakdown');
    
    // Warnings & Containers
    const setupWarning = document.getElementById('setup-warning');
    const chartsContainer = document.getElementById('charts-container');
    const tableContainer = document.getElementById('table-container');
    const tableSearch = document.getElementById('table-search');
    const btnExportCsv = document.getElementById('btn-export-csv');
    const consoleLogs = document.getElementById('console-logs');
    const btnClearLogs = document.getElementById('btn-clear-logs');
    
    // Modal
    const chartModal = document.getElementById('chart-modal');
    const modalImg = document.getElementById('modal-img');
    const modalTitle = document.getElementById('modal-title');
    const modalCloseBtn = document.getElementById('modal-close-btn');
    const modalBackdrop = document.getElementById('modal-backdrop');

    // Navigation configuration
    const TabMetadata = {
        'overview': { title: 'Overview & Stats', desc: 'High-level financial KPIs and portfolio metrics' },
        'charts': { title: 'Data Visualizations', desc: 'Analytical charts generated by the pipeline' },
        'explorer': { title: 'Data Explorer', desc: 'View raw records of the master merged dataset' },
        'logs': { title: 'Pipeline Logs', desc: 'Console output from run_pipeline.py' }
    };

    // Initialize UI
    function init() {
        setupNavigation();
        loadAllData();
        checkPipelineRunningStatus();
        setupEventListeners();
    }

    function setupNavigation() {
        navLinks.forEach(link => {
            link.addEventListener('click', (e) => {
                e.preventDefault();
                const tab = link.getAttribute('data-tab');
                switchTab(tab);
            });
        });
    }

    function switchTab(tabId) {
        activeTab = tabId;
        
        navLinks.forEach(l => {
            l.classList.toggle('active', l.getAttribute('data-tab') === tabId);
        });
        
        tabPanes.forEach(pane => {
            pane.classList.toggle('active', pane.id === `tab-${tabId}`);
        });

        const meta = TabMetadata[tabId];
        if (meta) {
            tabTitle.textContent = meta.title;
            tabDesc.textContent = meta.desc;
        }
    }

    function setupEventListeners() {
        btnRefresh.addEventListener('click', loadAllData);
        btnRunPipeline.addEventListener('click', runPipeline);
        
        btnClearLogs.addEventListener('click', () => {
            consoleLogs.textContent = 'Console cleared.';
        });

        // Filter charts category button clicks
        document.querySelectorAll('.btn-filter').forEach(btn => {
            btn.addEventListener('click', () => {
                document.querySelectorAll('.btn-filter').forEach(b => b.classList.remove('active'));
                btn.classList.add('active');
                renderCharts(btn.getAttribute('data-filter'));
            });
        });

        // Table search input
        tableSearch.addEventListener('input', renderTable);

        // Export download trigger
        btnExportCsv.addEventListener('click', () => {
            window.open('/api/download-master', '_blank');
        });

        // Modal triggers
        modalCloseBtn.addEventListener('click', closeModal);
        modalBackdrop.addEventListener('click', closeModal);
        document.addEventListener('keydown', (e) => {
            if (e.key === 'Escape') closeModal();
        });
    }

    // Modal view helpers
    function openModal(chart) {
        modalTitle.textContent = chart.title;
        modalImg.src = chart.url;
        chartModal.classList.add('open');
    }

    function closeModal() {
        chartModal.classList.remove('open');
        modalImg.src = '';
    }

    // Fetch dashboard components
    async function loadAllData() {
        refreshIcon.classList.add('fa-spin');
        try {
            await Promise.all([
                fetchStats(),
                fetchCharts(),
                fetchDataPreview()
            ]);
            lastUpdated.textContent = `Last Updated: ${new Date().toLocaleTimeString()}`;
        } catch (e) {
            console.error('Failed to reload dashboard:', e);
        } finally {
            setTimeout(() => refreshIcon.classList.remove('fa-spin'), 400);
        }
    }

    async function fetchStats() {
        try {
            const res = await fetch('/api/stats');
            const data = await res.json();

            if (!data.initialized) {
                setupWarning.classList.remove('hidden');
                resetKPIValues();
                return;
            }

            setupWarning.classList.add('hidden');

            // Format Amount
            const amtCr = data.total_disbursed / 10000000;
            const amtLakhs = data.total_disbursed / 100000;
            const displayAmt = amtCr >= 1 ? `₹${amtCr.toFixed(2)} Cr` : `₹${amtLakhs.toFixed(1)} L`;

            kpiCustomers.textContent = data.total_customers.toLocaleString();
            kpiLoans.textContent = data.total_loans.toLocaleString();
            kpiAmount.textContent = displayAmt;
            kpiDefault.textContent = `${data.default_rate.toFixed(2)}%`;
            kpiCredit.textContent = data.avg_credit_score.toFixed(0);
            kpiRate.textContent = `${data.avg_interest_rate.toFixed(2)}%`;

            // Draw distributions
            drawRiskList(data.risk_counts, data.total_loans);
            drawStatusList(data.status_counts, data.total_loans);

        } catch (err) {
            console.error('Stats fetch error:', err);
            resetKPIValues();
        }
    }

    function resetKPIValues() {
        kpiCustomers.textContent = '-';
        kpiLoans.textContent = '-';
        kpiAmount.textContent = '-';
        kpiDefault.textContent = '-';
        kpiCredit.textContent = '-';
        kpiRate.textContent = '-';
        riskBreakdown.innerHTML = '<div class="no-data">Data unavailable</div>';
        statusBreakdown.innerHTML = '<div class="no-data">Data unavailable</div>';
    }

    function drawRiskList(counts, total) {
        riskBreakdown.innerHTML = '';
        if (!counts || total === 0) return;

        const tiers = [
            { name: 'Low', color: 'var(--color-success)' },
            { name: 'Medium', color: 'var(--color-warning)' },
            { name: 'High', color: 'var(--color-danger)' }
        ];

        const wrapper = document.createElement('div');
        wrapper.className = 'breakdown-list';

        tiers.forEach(tier => {
            const val = counts[tier.name] || 0;
            const pct = (val / total) * 100;
            
            const item = document.createElement('div');
            item.className = 'breakdown-item';
            item.innerHTML = `
                <div class="breakdown-info">
                    <span>${tier.name} Risk</span>
                    <span>${val} (${pct.toFixed(1)}%)</span>
                </div>
                <div class="breakdown-bar">
                    <div class="breakdown-fill" style="width: ${pct}%; background-color: ${tier.color}"></div>
                </div>
            `;
            wrapper.appendChild(item);
        });

        riskBreakdown.appendChild(wrapper);
    }

    function drawStatusList(counts, total) {
        statusBreakdown.innerHTML = '';
        if (!counts || total === 0) return;

        const scheme = {
            'Active': 'var(--color-accent)',
            'Closed': 'var(--color-success)',
            'Defaulted': 'var(--color-danger)',
            'Under Review': 'var(--color-warning)'
        };

        const wrapper = document.createElement('div');
        wrapper.className = 'breakdown-list';

        Object.keys(counts).forEach(k => {
            const val = counts[k] || 0;
            const pct = (val / total) * 100;
            const color = scheme[k] || 'var(--color-text-sub)';

            const item = document.createElement('div');
            item.className = 'breakdown-item';
            item.innerHTML = `
                <div class="breakdown-info">
                    <span>${k}</span>
                    <span>${val} (${pct.toFixed(1)}%)</span>
                </div>
                <div class="breakdown-bar">
                    <div class="breakdown-fill" style="width: ${pct}%; background-color: ${color}"></div>
                </div>
            `;
            wrapper.appendChild(item);
        });

        statusBreakdown.appendChild(wrapper);
    }

    async function fetchCharts() {
        try {
            const res = await fetch('/api/charts');
            chartsList = await res.json();
            
            // Set first filter default
            const activeFilter = document.querySelector('.btn-filter.active');
            renderCharts(activeFilter ? activeFilter.getAttribute('data-filter') : 'all');
        } catch (e) {
            console.error('Failed to get charts list:', e);
            chartsContainer.innerHTML = '<div class="no-data">Error loading visualization grid</div>';
        }
    }

    function renderCharts(categoryFilter) {
        chartsContainer.innerHTML = '';
        const filtered = chartsList.filter(c => categoryFilter === 'all' || c.category === categoryFilter);

        if (filtered.length === 0) {
            chartsContainer.innerHTML = '<div class="no-data">No charts available in this category</div>';
            return;
        }

        filtered.forEach(chart => {
            const card = document.createElement('div');
            card.className = 'chart-card';

            const visual = chart.exists
                ? `<div class="chart-img-wrap"><img src="${chart.url}" alt="${chart.title}" loading="lazy"></div>`
                : `<div class="no-data" style="padding: 40px 0;">Image pending pipeline execution</div>`;

            card.innerHTML = `
                ${visual}
                <div class="chart-meta">
                    <div class="chart-title-row">
                        <h4 class="chart-card-title">${chart.title}</h4>
                        <span class="chart-category">${chart.category}</span>
                    </div>
                    <p class="chart-desc">${chart.description}</p>
                </div>
            `;

            if (chart.exists) {
                card.querySelector('.chart-img-wrap').addEventListener('click', () => {
                    openModal(chart);
                });
            }

            chartsContainer.appendChild(card);
        });
    }

    async function fetchDataPreview() {
        try {
            const res = await fetch('/api/data-preview');
            const data = await res.json();

            if (data.initialized) {
                fullDataset = data.data || [];
                tableColumns = data.columns || [];
            } else {
                fullDataset = [];
                tableColumns = [];
            }

            renderTable();
        } catch (e) {
            console.error('Explorer table reload error:', e);
        }
    }

    function renderTable() {
        tableContainer.innerHTML = '';
        if (fullDataset.length === 0) {
            tableContainer.innerHTML = '<div class="no-data">No dataset generated. Please run the pipeline first.</div>';
            btnExportCsv.disabled = true;
            return;
        }

        btnExportCsv.disabled = false;

        const query = tableSearch.value.trim().toLowerCase();
        const filtered = fullDataset.filter(row => {
            if (!query) return true;
            return Object.values(row).some(val => String(val).toLowerCase().includes(query));
        });

        if (filtered.length === 0) {
            tableContainer.innerHTML = '<div class="no-data">No records matching search query</div>';
            return;
        }

        const table = document.createElement('table');
        table.className = 'data-table';
        
        const thead = document.createElement('thead');
        const headerRow = document.createElement('tr');
        tableColumns.forEach(col => {
            const th = document.createElement('th');
            th.textContent = col.title;
            headerRow.appendChild(th);
        });
        thead.appendChild(headerRow);
        table.appendChild(thead);

        const tbody = document.createElement('tbody');
        filtered.slice(0, 100).forEach(row => {
            const tr = document.createElement('tr');
            tableColumns.forEach(col => {
                const td = document.createElement('td');
                td.textContent = row[col.field] ?? '';
                tr.appendChild(td);
            });
            tbody.appendChild(tr);
        });
        table.appendChild(tbody);

        tableContainer.appendChild(table);
    }

    // Pipeline runner controls
    function setRunningState(running) {
        isRunning = running;
        btnRunPipeline.disabled = running;
        
        const label = btnRunPipeline.querySelector('span') || btnRunPipeline;
        const icon = btnRunPipeline.querySelector('i');
        
        if (running) {
            btnRunPipeline.innerHTML = '<i class="fa-solid fa-spinner fa-spin"></i> Running...';
            pipelineStatusDot.className = 'status-indicator running';
            pipelineStatusText.textContent = 'Pipeline Status: Running';
        } else {
            btnRunPipeline.innerHTML = '<i class="fa-solid fa-play"></i> Run Pipeline';
        }
    }

    async function runPipeline() {
        if (isRunning) return;
        setRunningState(true);
        consoleLogs.textContent = '[LAUNCH] Subprocess started...\n';
        switchTab('logs');

        try {
            const res = await fetch('/api/run-pipeline', { method: 'POST' });
            const data = await res.json();
            
            if (data.status === 'started' || data.status === 'already_running') {
                pollPipeline();
            } else {
                setRunningState(false);
            }
        } catch (e) {
            console.error('Trigger pipeline failed:', e);
            setRunningState(false);
        }
    }

    async function checkPipelineRunningStatus() {
        try {
            const res = await fetch('/api/pipeline-status');
            const data = await res.json();
            
            if (data.status === 'running') {
                setRunningState(true);
                pollPipeline();
            } else {
                setRunningState(false);
                updatePulseWidget(data.status);
                fetchLogs();
            }
        } catch (e) {
            console.error('Error checking status:', e);
        }
    }

    function pollPipeline() {
        if (pipelinePollInterval) clearInterval(pipelinePollInterval);
        
        pipelinePollInterval = setInterval(async () => {
            try {
                const res = await fetch('/api/pipeline-status');
                const data = await res.json();
                
                await fetchLogs();

                if (data.status !== 'running') {
                    clearInterval(pipelinePollInterval);
                    setRunningState(false);
                    updatePulseWidget(data.status);
                    loadAllData();
                }
            } catch (e) {
                console.error('Polling execution error:', e);
                clearInterval(pipelinePollInterval);
                setRunningState(false);
            }
        }, 1000);
    }

    async function fetchLogs() {
        try {
            const res = await fetch('/api/pipeline-logs');
            const data = await res.json();
            if (data.logs) {
                consoleLogs.textContent = data.logs;
                consoleLogs.scrollTop = consoleLogs.scrollHeight;
            }
        } catch (e) {
            console.error('Failed to load logs:', e);
        }
    }

    function updatePulseWidget(status) {
        pipelineStatusDot.className = `status-indicator ${status}`;
        let label = 'Idle';
        if (status === 'success') label = 'Completed';
        if (status === 'error') label = 'Error';
        pipelineStatusText.textContent = `Pipeline Status: ${label}`;
    }

    // Initialize application
    init();
});
